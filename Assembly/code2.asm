INCLUDE 'emu8086.inc'
ORG 100h
MAIN PROC
 MOV AH,1
 PRINT 'ENTER THE FIRST NUMBER: '
 INT 21h
 MOV BL,AL

 PRINT 'ENTER THE SECOND NUMBER: '
 INT 21h
 MOV BH,AL

 ADD BL,BH

 MOV AH,2
 MOV DL,0DH
 INT 21h
 MOV DL,0AH
 INT 21h

 PRINT 'SUM: '
 MOV AH,2
 SUB BL,48
 MOV DL,BL
 INT 21h

 MOV AH,4CH
 INT 21h

 MAIN ENDP
END MAIN