INCLUDE 'emu8086.inc'
ORG 100h
.DATA
 ARA DB 5 DUP(?)

.CODE
 MAIN PROC
 MOV AX,@DATA
 MOV DS,AX

 MOV CX,5
 MOV SI, OFFSET ARA
 PRINT 'ENTER THE NUMBERS:'

 L1:
 MOV AH,1
 INT 21h

 MOV [SI],AL
 INC SI
 LOOP L1

 MOV DL,10
 MOV AH,2
 INT 21h

 MOV DL,13
 MOV AH,2
 INT 21h

 PRINT 'ARRAY IS :'

 MOV SI ,OFFSET ARA
 MOV CX,5
 L2:
 MOV DL,[SI]
 MOV AH,2
 INT 21h

 MOV DL,32
 MOV AH,2
 INT 21h

 INC SI
 LOOP L2

 MAIN ENDP
END MAIN